x = 6
y = 2
z = 7

# Operaciones básicas
print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")

# División al Piso
print(f"{z} dividido al piso de {y} es igual a {z//y}")

# Módulo
print(f"{z} módulo de {y} es igual a {z%y}")

# Potencia
print(f"{z} elevado a la {y} es igual a {z**y}")

# Raíz cuadrada
print(f"La raíz cuadrada de {x} es {x**0.5}")
